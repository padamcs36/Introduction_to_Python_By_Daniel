import math

print(math.fabs(-2)) #absolute value in float

print(math.ceil(3.43)) #round up to its nearest value integer
print(math.ceil(-3.4))

print(math.floor(3.21)) # round down to its nearest value integer
print(math.floor(-4.3))

print(math.e) #value of e = 2.718
print(math.pi)

print(math.exp(1)) # e^x
print(math.log(3.3))
print(math.log(100, 10))
print(math.sqrt(36))
print(math.sin(90))
print(math.asin(1))

print(math.degrees(1.57)) # convert angle x from radians to degree
print(math.radians(90)) #convert angle x from degree to radians

print(math.cos(2 * math.pi))
print(math.cos(2 * math.pi))
